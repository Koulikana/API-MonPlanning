--CREATE DATABASE MonPlanning

USE MonPlanning

CREATE TABLE Employee
(
	matricule INT IDENTITY(1,1) NOT NULL,
	firstName VARCHAR(300) NOT NULL,
	lastName VARCHAR(300) NOT NULL,
	birthDate DATE NOT NULL,
	email VARCHAR(300) NOT NULL,
	password VARCHAR(300) NOT NULL,
	CONSTRAINT PK_EMPLOYEE PRIMARY KEY(matricule)
);

CREATE TABLE Month
(
	monthId INT NOT NULL,
	month VARCHAR(30) NOT NULL,
	daysCount INT NOT NULL,
	leap BIT NOT NULL,
	CONSTRAINT PK_MONTH PRIMARY KEY(month)
);

CREATE TABLE ScheduleOption
(
	code VARCHAR(30) NOT NULL,
	value VARCHAR(300) NOT NULL,
	CONSTRAINT PK_SCHEDULEOPTION PRIMARY KEY(code)
);